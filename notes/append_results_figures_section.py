#!/usr/bin/env python3
"""Append a cautious results-presentation and figure-design section to paper/draft.md.

Research-only helper: this script does not publish or upload anything.
"""
from __future__ import annotations

import hashlib
import json
from datetime import datetime, timezone
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
DRAFT = ROOT / "paper" / "draft.md"
OUT = ROOT / "notes" / "paper-results-figures-section-check-output.json"
MARKER = "## Limitations and reproducibility"
SECTION_HEADING = "## Results presentation and figure design"
FORBIDDEN = [
    "Zetes",
    "certifies compliance",
    "proves legal compliance",
    "official audit",
    "guarantees",
    "we offer eIDAS-aligned trust services",
    "qualified trust service provider",
    "QTSP",
]

SECTION = """## Results presentation and figure design

The results section should be organized around what the fixed workflow observed, not around interpretations of individual trusted-list entries. A compact run table can show, for each date, pointer attempts, fetch successes, endpoint errors, XML-normalization successes, non-XML skips, parser errors, comparable records, and diff entries. This table is the safest first figure because every cell can be traced to a local manifest or alert entry, and none of the cells requires naming a listed provider or service in prose.

A second figure can summarize diff classes over time once multiple dated runs exist. Stacked bars or small multiples by territory code would let the draft distinguish document-level churn from provider/service inventory churn, while preserving the policy that listed names remain inside machine-readable records only. Captions should describe the data as locally observed structural telemetry generated by Cassandra's parser and diff engine. They should not imply that a taller bar represents legal importance, compliance risk, supervisory concern, or relying-party trustworthiness.

For narrative results, the draft should prefer aggregate statements with explicit evidence pointers. Acceptable examples include: a dated run recorded zero structural diff entries against the configured baseline, a parser run skipped a stated number of non-XML artifacts, or an evidence bundle locally verified its listed hashes. Named case studies should remain out of the main prose until the operator approves both the example and its wording. If later review authorizes a case study, it should be framed as a document-structure example, not as a statement about the status of the named organization or service.

The figure pipeline should therefore be reproducible and boring: derive CSV or JSON summaries from existing manifests, render static plots from those summaries, record hashes for both the data table and the image, and cite the corresponding bundle. The absence of a plot is preferable to a visually persuasive chart whose caption overstates what the evidence supports.
"""


def sha256(path: Path) -> str:
    h = hashlib.sha256()
    with path.open("rb") as f:
        for chunk in iter(lambda: f.read(1024 * 1024), b""):
            h.update(chunk)
    return h.hexdigest()


def word_count(text: str) -> int:
    body = text.split("\n", 1)[1] if "\n" in text else text
    return len([token for token in body.replace("/", " ").split() if token.strip()])


def main() -> None:
    original = DRAFT.read_text(encoding="utf-8")
    if SECTION_HEADING in original:
        updated = original
        changed = False
    else:
        if MARKER not in original:
            raise SystemExit(f"marker not found: {MARKER}")
        updated = original.replace(MARKER, SECTION + "\n" + MARKER, 1)
        DRAFT.write_text(updated, encoding="utf-8")
        changed = True

    forbidden_hits = [phrase for phrase in FORBIDDEN if phrase in updated]
    result = {
        "status": "ok" if not forbidden_hits else "error",
        "changed": changed,
        "created": datetime.now(timezone.utc).isoformat().replace("+00:00", "Z"),
        "draft_path": str(DRAFT.relative_to(ROOT)),
        "draft_sha256": sha256(DRAFT),
        "section_heading": SECTION_HEADING,
        "section_word_count": word_count(SECTION),
        "forbidden_hits": forbidden_hits,
        "checks": {
            "has_tych_institute_affiliation": "Affiliation: Tyche Institute, Tallinn, Estonia" in updated,
            "mentions_zetes": "Zetes" in updated,
            "uses_aggregate_only_named_entity_policy": "listed names remain inside machine-readable records only" in SECTION,
            "avoids_status_interpretation": "not as a statement about the status" in SECTION,
            "requires_evidence_pointers": "explicit evidence pointers" in SECTION,
        },
        "caveats": [
            "research-only structural observation",
            "figures summarize local telemetry only",
            "no relying-party validation claim",
            "no legal-status determination",
            "no listed-entity names in narrative prose without approval",
        ],
    }
    OUT.write_text(json.dumps(result, indent=2, sort_keys=True) + "\n", encoding="utf-8")
    print(json.dumps(result, indent=2, sort_keys=True))
    if (
        forbidden_hits
        or not result["checks"]["has_tych_institute_affiliation"]
        or result["checks"]["mentions_zetes"]
        or not result["checks"]["uses_aggregate_only_named_entity_policy"]
        or not result["checks"]["avoids_status_interpretation"]
        or not result["checks"]["requires_evidence_pointers"]
    ):
        raise SystemExit(1)


if __name__ == "__main__":
    main()
